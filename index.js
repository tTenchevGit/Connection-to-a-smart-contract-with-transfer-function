async function connect() {
  if (typeof window.ethereum !== "undefined") {
    try {
      await ethereum.request({ method: "eth_requestAccounts" });
      document.getElementById("connectButton").innerHTML = "Connected";
      const accounts = await ethereum.request({ method: "eth_accounts" });
      console.log(accounts);
    } catch (error) {
      console.error("Connection Error:", error);
      document.getElementById("connectButton").innerHTML = "Connection Failed";
    }
  } else {
    document.getElementById("connectButton").innerHTML = "Please install MetaMask";
  }
}

async function execute() {
  if (typeof window.ethereum !== "undefined") {
    const provider = new ethers.providers.Web3Provider(window.ethereum);
    const signer = provider.getSigner();
    const contract = new ethers.Contract(contractAddress, abi, signer);
    try {
      await contract.store(42);
    } catch (error) {
      console.error("Execution Error:", error);
      document.getElementById("executeButton").innerHTML = "Execution Failed";
    }
  } else {
    document.getElementById("executeButton").innerHTML = "Please install MetaMask";
  }
}

async function transferTokens() {
  if (typeof window.ethereum !== "undefined") {
    const address = document.getElementById('address').value;
    const amount = document.getElementById('amount').value;
    const provider = new ethers.providers.Web3Provider(window.ethereum);
    const signer = provider.getSigner();
    const contract = new ethers.Contract(contractAddress, abi, signer);

    try {
      const transactionResponse = await contract.transfer(address, ethers.utils.parseUnits(amount, "ether"));
      await transactionResponse.wait();
      alert('Transfer successful!');
    } catch (error) {
      console.error('Transfer Error:', error);
      alert('Transfer failed. Check console for details.');
    }
  } else {
    alert('MetaMask is not available.');
  }
}

